    def render(self, win):
        y = 0
        color = (0, 0, 0)
        for xline in self.maze_map:
            x = 0
            for loc in xline:
                tile_floor = Tile()
                tile_floor.dye(loc)
                tile_floor.location(x, y)
                self.wall.add(tile_floor)
                x += 1
            y += 1
        self.wall.draw(self.maze)
        win.blit(self.maze, (0, 0))


